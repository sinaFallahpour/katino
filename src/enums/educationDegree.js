export function educationDegree(type) {
  switch (type) {
    case 1:
      return "مهم نیست";
      break;

    case 2:
      return "دیپلم";
      break;

    case 3:
      return "کاردانی";
      break;

    case 4:
      return "کارشناسی";
      break;

    case 5:
      return "کارشناسی ارشد";
      break;

    case 6:
      return "دکترا";
      break;
  }
}
